word=str(input("enter a word"))
for char in word: 
         if(char=="a" or char=="A"):
             print("a" )
         elif(char=="e" or char=="E"):
            print("e")
         elif(char=="i" or char=="I"):
            print("i")
         elif(char=="o"or char=="O"):
             print("o")
         elif(char=="u" or char=="U"):
             print("u")
         
